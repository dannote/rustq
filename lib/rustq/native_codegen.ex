            A.arm A.lit_pat("and") do
              A.return(
                A.path_call([:super, :parse_expr_tokens], [
                  A.token_macro(:quote, "#left && #right")
                ])
              )
            end

            A.arm A.lit_pat("or") do
              A.return(
                A.path_call([:super, :parse_expr_tokens], [
                  A.token_macro(:quote, "#left || #right")
                ])
              )
            end

            A.arm A.wildcard() do
              A.return(A.err(A.path([:rustler, :Error, :BadArg])))
            end
          end
        end
      end,
      function :decode_expr_match,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Expr>" do
        A.let(:expr, A.try(A.path_call([:super, :decode_expr], [map_get(:term, "expr")])))
        A.let(:arms, A.try(A.path_call([:super, :decode_arm_list], [map_get(:term, "arms")])))

        A.return(
          A.path_call([:super, :parse_expr_tokens], [
            A.token_macro(:quote, "match #expr { #(#arms)* }")
          ])
        )
      end,
      function :decode_expr_if,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Expr>" do
        A.let(
          :condition,
          A.try(A.path_call([:super, :decode_expr], [map_get(:term, "condition")]))
        )

        A.let(:then_block, A.try(A.path_call([:super, :decode_block], [map_get(:term, "then")])))
        A.let(:else_block, A.try(A.path_call([:super, :decode_block], [map_get(:term, "else")])))

        A.return(
          A.path_call([:super, :parse_expr_tokens], [
            A.token_macro(:quote, "if #condition #then_block else #else_block")
          ])
        )
      end,
      function :decode_expr_tuple,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Expr>" do
        A.let(
          :values,
          A.try(A.path_call([:super, :decode_expr_list], [map_get(:term, "values")]))
        )

        A.return(
          A.path_call([:super, :parse_expr_tokens], [A.token_macro(:quote, "(#(#values),*)")])
        )
      end
    ]
  end

  defp unary_pat_decoder(name, field, tokens) do
    function name,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Pat>" do
      A.let(:pat, A.try(A.path_call([:super, :decode_pat], [map_get(:term, field)])))
      A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, tokens)]))
    end
  end

  defp unary_expr_decoder(name, field, tokens) do
    function name,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Expr>" do
      A.let(:expr, A.try(A.path_call([:super, :decode_expr], [map_get(:term, field)])))
      A.return(A.path_call([:super, :parse_expr_tokens], [A.token_macro(:quote, tokens)]))
    end
  end

  defp path_parts_term(term) do
    A.try(A.method(map_get(term, "path"), :map_get, [atom_expr(term, "parts")]))
  end

  defp map_get(receiver, key) do
    A.try(A.method(receiver, :map_get, [atom_expr(receiver, key)]))
  end

  defp atom_expr(receiver, key) do
    A.try(A.path_call([:super, :atom], [A.method(receiver, :get_env), key]))
  end

  defp format_rust(source) do
    path =
      Path.join(System.tmp_dir!(), "rustq_generated_#{System.unique_integer([:positive])}.rs")

    File.write!(path, source)

    try do
      case System.cmd("rustfmt", ["--edition", "2021", path], stderr_to_stdout: true) do
        {_output, 0} -> File.read!(path)
        {_output, _status} -> source
      end
    after
      File.rm(path)
    end
  rescue
    ErlangError -> source
  end
