  end

  defp decode_ast_item_item do
    %AST.Function{
      name: :decode_ast_item,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Item>",
      body:
        A.block do
          A.return do
            A.match A.method(A.try(A.call(:struct_name, [:term])), :as_str) do
              decode_ast_item_arms()
            end
          end
        end
    }
  end

  defp decode_ast_item_arms do
    Schema.nodes(:item)
    |> Enum.map(&decode_ast_item_arm/1)
    |> Kernel.++([
      A.arm A.wildcard() do
        A.return(A.err(A.path([:rustler, :Error, :BadArg])))
      end
    ])
  end

  defp decode_ast_item_arm(%Schema.Node{name: :macro_item, rust_const: rust_const}) do
    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(A.path_call([:super, :decode_ast_macro_item], [:term]))
    end
  end

  defp decode_ast_item_arm(%Schema.Node{name: name, rust_const: rust_const}) do
    {wrapper, decoder} = item_decoder(name)

    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(
        A.ok(
          A.path_call([:Item, wrapper], [
            A.try(A.path_call([:super, decoder], [:term]))
          ])
        )
      )
    end
  end

  defp item_decoder(:use), do: {:Use, :decode_ast_use}
  defp item_decoder(:module), do: {:Mod, :decode_ast_module}
  defp item_decoder(:const), do: {:Const, :decode_ast_const}
  defp item_decoder(:function), do: {:Fn, :decode_ast_function}
  defp item_decoder(:struct), do: {:Struct, :decode_ast_struct}
  defp item_decoder(:enum), do: {:Enum, :decode_ast_enum}

  defp decode_ast_type_item do
    %AST.Function{
      name: :decode_ast_type,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Type>",
      body:
        A.block do
          A.return do
            A.match A.method(A.try(A.call(:struct_name, [:term])), :as_str) do
              decode_ast_type_arms()
            end
          end
        end
    }
  end

  defp decode_ast_type_arms do
    Schema.nodes(:type)
    |> Enum.map(&decode_ast_type_arm/1)
    |> Kernel.++([
      A.arm A.wildcard() do
        A.return(A.err(A.path([:rustler, :Error, :BadArg])))
      end
    ])
  end

  defp decode_ast_type_arm(%Schema.Node{name: name, rust_const: rust_const}) do
    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(A.path_call([:super, type_decoder(name)], [:term]))
    end
  end

  defp type_decoder(name), do: String.to_atom("decode_#{name}")

  defp decode_ast_pat_item do
    %AST.Function{
      name: :decode_ast_pat,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Pat>",
      body:
        A.block do
          A.return do
            A.match A.method(A.try(A.call(:struct_name, [:term])), :as_str) do
              decode_ast_pat_arms()
            end
          end
        end
    }
  end

  defp decode_ast_pat_arms do
    Schema.nodes(:pat)
    |> Enum.map(&decode_ast_pat_arm/1)
    |> Kernel.++([
      A.arm A.wildcard() do
        A.return(A.err(A.path([:rustler, :Error, :BadArg])))
      end
    ])
  end

  defp decode_ast_pat_arm(%Schema.Node{name: name, rust_const: rust_const}) do
    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(A.path_call(pat_decoder_path(name), [:term]))
    end
  end

  defp pat_decoder_path(name)
       when name in [:pat_atom_guard, :pat_path_tuple, :pat_struct, :pat_tuple],
       do: [:super, pat_decoder(name)]

  defp pat_decoder_path(name), do: [pat_decoder(name)]

  defp pat_decoder(name), do: String.to_atom("decode_#{name}")

  defp decode_pat_helper_items do
    [
      %AST.Function{
        name: :decode_pat_var,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Pat>",
        body:
          A.block do
            A.let(
              :ident,
              A.token_macro([:quote, :format_ident], ~s|"{}", super::atom_key(term, "name")?|)
            )

            A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, "#ident")]))
          end
      },
      %AST.Function{
        name: :decode_pat_wildcard,
        vis: :crate,
        args: [_term: "Term"],
        returns: "NifResult<Pat>",
        body:
          A.block do
            A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, "_")]))
          end
      },
      %AST.Function{
        name: :decode_pat_none,
        vis: :crate,
        args: [_term: "Term"],
        returns: "NifResult<Pat>",
        body:
          A.block do
            A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, "None")]))
          end
      },
      %AST.Function{
        name: :decode_pat_path,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Pat>",
        body:
          A.block do
            A.let(
              :path,
              A.try(
                A.path_call([:super, :parse_path], [
                  A.ref(A.try(A.path_call([:super, :path_parts], [path_parts_term(:term)])))
                ])
              )
            )

            A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, "#path")]))
          end
      },
      %AST.Function{
        name: :decode_pat_literal,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<Pat>",
        body:
          A.block do
            A.return(A.path_call([:super, :decode_pat_literal_value], [map_get(:term, "value")]))
          end
      },
      unary_pat_decoder(:decode_pat_some, "pattern", "Some(#pat)"),
      unary_pat_decoder(:decode_pat_ok, "pattern", "Ok(#pat)"),
      unary_pat_decoder(:decode_pat_err, "pattern", "Err(#pat)")
    ]
  end

  defp unary_pat_decoder(name, field, tokens) do
    %AST.Function{
      name: name,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Pat>",
      body:
        A.block do
          A.let(:pat, A.try(A.path_call([:super, :decode_pat], [map_get(:term, field)])))
          A.return(A.path_call([:super, :parse_pat], [A.token_macro(:quote, tokens)]))
        end
    }
  end

  defp path_parts_term(term) do
    A.try(A.method(map_get(term, "path"), :map_get, [atom_expr(term, "parts")]))
  end

  defp map_get(receiver, key) do
    A.try(A.method(receiver, :map_get, [atom_expr(receiver, key)]))
  end

  defp atom_expr(receiver, key) do
    A.try(A.path_call([:super, :atom], [A.method(receiver, :get_env), key]))
  end

  defp format_rust(source) do
    path =
      Path.join(System.tmp_dir!(), "rustq_generated_#{System.unique_integer([:positive])}.rs")

    File.write!(path, source)

    try do
      case System.cmd("rustfmt", ["--edition", "2021", path], stderr_to_stdout: true) do
        {_output, 0} -> File.read!(path)
        {_output, _status} -> source
      end
    after
      File.rm(path)
    end
  rescue
    ErlangError -> source
  end
end
