        returns: "NifResult<Option<Term<'a>>>",
        body:
          A.block do
            A.return do
              A.match A.method(:term, :map_get, [
                        A.try(A.call(:atom, [A.method(:term, :get_env), :key]))
                      ]) do
                A.arm A.ok_pat(:value) do
                  A.return(A.ok(A.some(:value)))
