      @ast_modules
      |> Enum.map_join("\n", fn {name, module} ->
        "    pub(crate) const #{constant_name(name)}: &str = #{inspect(module)};"
      end)

    ast_helpers =
      helper_items()
      |> Enum.map_join("\n", &AST.render_item_native/1)

    """
    // This file is generated by RustQ. Do not edit by hand.

    use rustler::{Atom, Env, NifResult, Term};

    pub(crate) mod atoms {
        rustler::atoms! {
            ok,
            error,
        }
    }

    pub(crate) mod ast_modules {
    #{constants}
    }

    #{String.trim_trailing(ast_helpers)}
    """
    |> format_rust()
  end

