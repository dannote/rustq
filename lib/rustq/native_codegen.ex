defmodule RustQ.NativeCodegen do
  @moduledoc false

  alias RustQ.Rust.AST
  alias RustQ.Rust.AST.Builder, as: A
  alias RustQ.Rust.AST.Schema

  require A

  def generated_ast_support do
    [
      A.use("rustler::{Atom, Env, NifResult, Term}"),
      A.use("syn::{Item, Type}"),
      atoms_module(),
      ast_modules_module(),
      helper_items(),
      decode_ast_item_item(),
      decode_ast_type_item()
    ]
    |> List.flatten()
    |> AST.render_file_native()
    |> then(&"// This file is generated by RustQ. Do not edit by hand.\n\n#{&1}")
    |> format_rust()
  end

  defp atoms_module do
    A.module(
      :atoms,
      [
        A.macro_item("rustler::atoms! {\n    ok,\n    error,\n}")
      ],
      vis: :crate
    )
  end

  defp ast_modules_module do
    constants =
      Enum.map(Schema.nodes(), fn node ->
        A.const(node.rust_const, "&str", node.rust_module, vis: :crate)
      end)

    A.module(:ast_modules, constants, vis: :crate)
  end

  defp helper_items do
    [
      %AST.Function{
        name: :atom,
        vis: :crate,
        args: [env: "Env", name: "&str"],
        returns: "NifResult<Atom>",
        body:
          A.block do
            A.return(A.path_call([:Atom, :from_str], [:env, :name]))
          end
      },
      %AST.Function{
        name: :optional_map_get,
        vis: :crate,
        lifetime: :a,
        args: [term: %AST.TypePath{parts: [:Term], lifetimes: [:a]}, key: "&str"],
        returns: "NifResult<Option<Term<'a>>>",
        body:
          A.block do
            A.return do
              A.match A.method(:term, :map_get, [
                        A.try(A.call(:atom, [A.method(:term, :get_env), :key]))
                      ]) do
                A.arm A.ok_pat(:value) do
                  A.return(A.ok(A.some(:value)))
                end

                A.arm A.err_pat(A.wildcard()) do
                  A.return(A.ok(A.none()))
                end
              end
            end
          end
      },
      %AST.Function{
        name: :atom_key,
        vis: :crate,
        args: [term: "Term", key: "&str"],
        returns: "NifResult<String>",
        body:
          A.block do
            A.return(
              A.method(
                A.try(
                  A.method(:term, :map_get, [
                    A.try(A.call(:atom, [A.method(:term, :get_env), :key]))
                  ])
                ),
                :atom_to_string
              )
            )
          end
      },
      %AST.Function{
        name: :optional_atom_key,
        vis: :crate,
        args: [term: "Term", key: "&str"],
        returns: "NifResult<Option<String>>",
        body:
          A.block do
            A.let(
              :value,
              A.try(
                A.method(:term, :map_get, [
                  A.try(A.call(:atom, [A.method(:term, :get_env), :key]))
                ])
              )
            )

            A.return(
              A.if_expr(
                A.try(A.call(:is_nil, [:value])),
                [A.return(A.ok(A.none()))],
                [A.return(A.ok(A.some(A.try(A.method(:value, :atom_to_string)))))]
              )
            )
          end
      },
      %AST.Function{
        name: :is_nil,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<bool>",
        body:
          A.block do
            A.return(
              A.ok(
                A.and_(
                  A.method(:term, :is_atom),
                  A.eq(A.try(A.method(:term, :atom_to_string)), "nil")
                )
              )
            )
          end
      },
      %AST.Function{
        name: :struct_name,
        vis: :crate,
        args: [term: "Term"],
        returns: "NifResult<String>",
        body:
          A.block do
            A.return(
              A.method(
                A.try(
                  A.method(:term, :map_get, [
                    A.try(A.call(:atom, [A.method(:term, :get_env), "__struct__"]))
                  ])
                ),
                :atom_to_string
              )
            )
          end
      },
      %AST.Function{
        name: :expect_struct,
        vis: :crate,
        args: [term: "Term", expected: "&str"],
        returns: "NifResult<()>",
        body:
          A.block do
            A.return(
              A.if_expr(
                A.eq(A.try(A.call(:struct_name, [:term])), :expected),
                [A.return(A.ok())],
                [A.return(A.err(A.path([:rustler, :Error, :BadArg])))]
              )
            )
          end
      }
    ]
  end

  defp decode_ast_item_item do
    %AST.Function{
      name: :decode_ast_item,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Item>",
      body:
        A.block do
          A.return do
            A.match A.method(A.try(A.call(:struct_name, [:term])), :as_str) do
              decode_ast_item_arms()
            end
          end
        end
    }
  end

  defp decode_ast_item_arms do
    Schema.nodes(:item)
    |> Enum.map(&decode_ast_item_arm/1)
    |> Kernel.++([
      A.arm A.wildcard() do
        A.return(A.err(A.path([:rustler, :Error, :BadArg])))
      end
    ])
  end

  defp decode_ast_item_arm(%Schema.Node{name: :macro_item, rust_const: rust_const}) do
    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(A.path_call([:super, :decode_ast_macro_item], [:term]))
    end
  end

  defp decode_ast_item_arm(%Schema.Node{name: name, rust_const: rust_const}) do
    {wrapper, decoder} = item_decoder(name)

    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(
        A.ok(
          A.path_call([:Item, wrapper], [
            A.try(A.path_call([:super, decoder], [:term]))
          ])
        )
      )
    end
  end

  defp item_decoder(:use), do: {:Use, :decode_ast_use}
  defp item_decoder(:module), do: {:Mod, :decode_ast_module}
  defp item_decoder(:const), do: {:Const, :decode_ast_const}
  defp item_decoder(:function), do: {:Fn, :decode_ast_function}
  defp item_decoder(:struct), do: {:Struct, :decode_ast_struct}
  defp item_decoder(:enum), do: {:Enum, :decode_ast_enum}

  defp decode_ast_type_item do
    %AST.Function{
      name: :decode_ast_type,
      vis: :crate,
      args: [term: "Term"],
      returns: "NifResult<Type>",
      body:
        A.block do
          A.return do
            A.match A.method(A.try(A.call(:struct_name, [:term])), :as_str) do
              decode_ast_type_arms()
            end
          end
        end
    }
  end

  defp decode_ast_type_arms do
    Schema.nodes(:type)
    |> Enum.map(&decode_ast_type_arm/1)
    |> Kernel.++([
      A.arm A.wildcard() do
        A.return(A.err(A.path([:rustler, :Error, :BadArg])))
      end
    ])
  end

  defp decode_ast_type_arm(%Schema.Node{name: name, rust_const: rust_const}) do
    A.arm A.path_pat([:ast_modules, rust_const]) do
      A.return(A.path_call([:super, type_decoder(name)], [:term]))
    end
  end

  defp type_decoder(name), do: String.to_atom("decode_#{name}")

  defp format_rust(source) do
    path =
      Path.join(System.tmp_dir!(), "rustq_generated_#{System.unique_integer([:positive])}.rs")

    File.write!(path, source)

    try do
      case System.cmd("rustfmt", ["--edition", "2021", path], stderr_to_stdout: true) do
        {_output, 0} -> File.read!(path)
        {_output, _status} -> source
      end
    after
      File.rm(path)
    end
  rescue
    ErlangError -> source
  end
end
